/****** switch Statement *****/
// break is use to break code at given condition fulfilled.
 
let day = 2;
switch (day) {
    case 0:
        console.log("Sunday");

    case 1:
        console.log("Monday");

    case 2:
        console.log("Tuesday");

    case 3:
        console.log("Wednesday");

    case 4:
        console.log("Thursday");

    case 5:
        console.log("Friday");
    case 6:
        console.log("Saturday");
        break;
    default:
        console.log("Invalid Day");
        break;
}


