"use strict"
// intro to variables

// In variable we can store some information
// we can use use that information later
// we can also change that information later

// declare variable
var firstName = "Abdul";

// use a variable
console.log(firstName);

// change the variable
firstName = "Asma";
console.log(firstName);

// *** Important Notes
// variable can also store without var keyword
var lastName = "Idrisi";
console.log(lastName);

// lastname = "Abdul Idrisi";
lastName = "Abdul Idrisi";
console.log(lastName);

// In the above example var keyword not use but it is not good practice
//  To solve this problem we use "use strict" key words

